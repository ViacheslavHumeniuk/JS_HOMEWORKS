'use strict';

const operand1 = Number(prompt('Введіть перше число'));
const operand2 = Number(prompt('Введіть друге число'));

// +
const sumResult = operand1 + operand2;
console.log(`Результат: ${operand1} + ${operand2} = ${sumResult}`);

// -
const subtractionResult = operand1 - operand2;
console.log(`Результат: ${operand1} - ${operand2} = ${operand1 - operand2}`);
// *
const multiplicationResult = operand1 * operand2;
console.log(`Результат: ${operand1} * ${operand2} = ${operand1 * operand2}`);
// Ділення
const divisionResult = operand1 / operand2;
console.log(`Результат: ${operand1} / ${operand2} = ${operand1 / operand2}`);